// Obtém um elemento HTML pelo ID
const meuBotao = document.getElementById('meuBotao');

// Define um manipulador de evento de clique para o botão
meuBotao.addEventListener('click', function() {
  alert('Botão clicado!');
});


/*
Neste exemplo, quando o botão com o ID "meuBotao" é clicado, a função de callback será executada e exibirá um alerta.
A manipulação de eventos é fundamental para criar interações dinâmicas em páginas da web, como formulários interativos, 
atualizações de interface do usuário e muito mais.
*/